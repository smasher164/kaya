// kaya-gen: the Go arm of the generator family. Invoked by a
// go:generate directive beside a marked declaration —
//
//	//go:generate go run dev.kaya/cmd/kaya-gen -type Post -key string
//
// it reads the package's own declarations (the type is the schema;
// nothing is restated) and the declaration's shape decides what is
// generated, the one KayaGen story every language tells:
//
//   - an interface (the sealed marker over its implementing structs)
//     is a sum: the collection factory spelling the constructor order
//     exactly once, and the compile-total eliminator — a function with
//     one parameter per constructor. Go struct literals are open (an
//     omitted field is a nil func, not an error) but a call must
//     supply every argument, so totality is a compile error here, and
//     the scene checks it again.
//   - a struct is a record: the collection factory and a named-setter
//     patch (each setter records one update_field — a patch is
//     recorded writes, never a diff).
//
// The generated file is checked in (the go:generate convention);
// tools/gen-guests.sh --check regenerates and diffs, so a drifted
// file fails the gates rather than shipping.
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

// The wire vocabulary a field type maps into; any other field type is
// guest-only and gets no setter.
var wireTypes = map[string]bool{
	"string": true, "bool": true, "int64": true, "float64": true,
}

func main() {
	typ := flag.String("type", "", "the marked type: a sealed marker interface (sum) or a struct (record)")
	key := flag.String("key", "string", "the collection's key type")
	dir := flag.String("dir", "", "package directory (defaults to go:generate's cwd)")
	flag.Parse()
	if *typ == "" {
		fatal("kaya-gen: -type names a sealed marker interface (sum) or a struct (record)")
	}
	pkgDir := *dir
	if pkgDir == "" {
		pkgDir = "."
	}

	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, pkgDir, func(fi os.FileInfo) bool {
		return !strings.HasSuffix(fi.Name(), "_kaya.go")
	}, 0)
	if err != nil {
		fatal("kaya-gen: %v", err)
	}
	if len(pkgs) != 1 {
		fatal("kaya-gen: expected one package in %s, found %d", pkgDir, len(pkgs))
	}
	var pkg *ast.Package
	for _, p := range pkgs {
		pkg = p
	}

	// The declaration's shape decides sum or record.
	var iface *ast.InterfaceType
	var strct *ast.StructType
	for _, file := range pkg.Files {
		for _, decl := range file.Decls {
			gd, ok := decl.(*ast.GenDecl)
			if !ok || gd.Tok != token.TYPE {
				continue
			}
			for _, spec := range gd.Specs {
				ts := spec.(*ast.TypeSpec)
				if ts.Name.Name != *typ {
					continue
				}
				switch t := ts.Type.(type) {
				case *ast.InterfaceType:
					iface = t
				case *ast.StructType:
					strct = t
				default:
					fatal("kaya-gen: %s is neither an interface nor a struct", *typ)
				}
			}
		}
	}

	var b bytes.Buffer
	w := func(f string, args ...any) { fmt.Fprintf(&b, f+"\n", args...) }
	w("// Code generated by kaya-gen; DO NOT EDIT.")
	w("// Regenerate with `go generate ./...` (tools/gen-guests.sh checks freshness).")
	w("")
	w("package %s", pkg.Name)
	w("")
	w("import kaya \"dev.kaya/bindings/go\"")
	w("")

	var count int
	var what string
	switch {
	case iface != nil:
		count = generateSum(w, pkg, iface, *typ, *key)
		what = "constructors"
	case strct != nil:
		count = generateRecord(w, strct, *typ, *key)
		what = "fields"
	default:
		fatal("kaya-gen: no type named %s in %s", *typ, pkgDir)
	}

	out, err := format.Source(b.Bytes())
	if err != nil {
		fatal("kaya-gen: generated code does not format: %v", err)
	}
	path := filepath.Join(pkgDir, strings.ToLower(*typ)+"_kaya.go")
	if err := os.WriteFile(path, out, 0o644); err != nil {
		fatal("kaya-gen: %v", err)
	}
	fmt.Printf("kaya-gen: wrote %s (%d %s of %s)\n", path, count, what, *typ)
}

func generateSum(w func(string, ...any), pkg *ast.Package, iface *ast.InterfaceType, sum, key string) int {
	// The marker method: the sum interface's one (unexported) method.
	if len(iface.Methods.List) != 1 || len(iface.Methods.List[0].Names) != 1 {
		fatal("kaya-gen: %s must declare exactly one marker method", sum)
	}
	marker := iface.Methods.List[0].Names[0].Name

	// The constructors: structs with a value-receiver marker method,
	// ordered by source position — the declaration order is the
	// variant order, spelled nowhere else.
	type variant struct {
		name string
		pos  token.Pos
	}
	var variants []variant
	for _, file := range pkg.Files {
		for _, decl := range file.Decls {
			fd, ok := decl.(*ast.FuncDecl)
			if !ok || fd.Name.Name != marker || fd.Recv == nil || len(fd.Recv.List) != 1 {
				continue
			}
			recv, ok := fd.Recv.List[0].Type.(*ast.Ident)
			if !ok {
				continue
			}
			// Order by the struct declaration's position, not the
			// method's.
			pos := recv.Pos()
			for _, f2 := range pkg.Files {
				for _, d2 := range f2.Decls {
					gd, ok := d2.(*ast.GenDecl)
					if !ok || gd.Tok != token.TYPE {
						continue
					}
					for _, spec := range gd.Specs {
						ts := spec.(*ast.TypeSpec)
						if ts.Name.Name == recv.Name {
							pos = ts.Pos()
						}
					}
				}
			}
			variants = append(variants, variant{recv.Name, pos})
		}
	}
	if len(variants) < 2 {
		fatal("kaya-gen: %s needs two constructors or more, found %d", sum, len(variants))
	}
	sort.Slice(variants, func(i, j int) bool { return variants[i].pos < variants[j].pos })

	w("// %sCollection declares the sum collection: one variant per", sum)
	w("// constructor, in declaration order — this call is the one")
	w("// spelling of that order.")
	w("func %sCollection(tx *kaya.Tx) kaya.SumCollection[%s, %s] {", sum, key, sum)
	protos := make([]string, len(variants))
	for i, v := range variants {
		protos[i] = v.name + "{}"
	}
	w("\treturn kaya.SumOf[%s, %s](tx, %s)", key, sum, strings.Join(protos, ", "))
	w("}")
	w("")
	w("// %sEachSum is the template eliminator: one required arm per", sum)
	w("// constructor — a call must supply every argument, so totality is")
	w("// a compile error here, and the scene checks it again. An arm of")
	w("// `func(_ kaya.SumCase[...]) {}` renders its constructor as")
	w("// nothing, explicitly.")
	w("func %sEachSum(", sum)
	w("\ttx *kaya.Tx,")
	w("\tc kaya.SumCollection[%s, %s],", key, sum)
	for _, v := range variants {
		w("\t%s func(kaya.SumCase[%s, %s]),", lowerFirst(v.name), key, v.name)
	}
	w(") kaya.Widget {")
	w("\treturn tx.ForEach(c.Collection, func(t *kaya.Tpl) {")
	for _, v := range variants {
		w("\t\tc.Case[%s](t, %s)", v.name, lowerFirst(v.name))
	}
	w("\t})")
	w("}")

	// The refined per-constructor patches: re-eliminate at call time,
	// then write through the witnessed update.
	for _, v := range variants {
		fields := wireFieldsOf(pkg, v.name)
		if len(fields) == 0 {
			continue
		}
		w("")
		w("// %sAs%s re-eliminates at call time: the comma-ok is the", sum, v.name)
		w("// refinement, fresh at write time — a stale occurrence folds into")
		w("// the !ok arm — and each setter's update carries %s as its", v.name)
		w("// witness, asserted again by the scene.")
		w("func %sAs%s(tx *kaya.Tx, c kaya.SumCollection[%s, %s], key %s) (%s%sPatch, bool) {", sum, v.name, key, sum, key, lowerFirst(sum), v.name)
		w("\tif v, ok := c.Get(tx, key); ok {")
		w("\t\tif _, is := v.(%s); is {", v.name)
		w("\t\t\treturn %s%sPatch{tx: tx, c: c, key: key}, true", lowerFirst(sum), v.name)
		w("\t\t}")
		w("\t}")
		w("\treturn %s%sPatch{}, false", lowerFirst(sum), v.name)
		w("}")
		w("")
		w("type %s%sPatch struct {", lowerFirst(sum), v.name)
		w("\ttx  *kaya.Tx")
		w("\tc   kaya.SumCollection[%s, %s]", key, sum)
		w("\tkey %s", key)
		w("}")
		for _, f := range fields {
			w("")
			w("func (p %s%sPatch) %s(v %s) %s%sPatch {", lowerFirst(sum), v.name, f.name, f.typ, lowerFirst(sum), v.name)
			w("\tp.c.UpdateField(p.tx, p.key, func(t *%s) *%s { return &t.%s }, v)", v.name, f.typ, f.name)
			w("\treturn p")
			w("}")
		}
	}
	return len(variants)
}

// wireFieldsOf lists a named struct's exported wire-typed fields, in
// declaration order.
func wireFieldsOf(pkg *ast.Package, name string) []struct{ name, typ string } {
	var fields []struct{ name, typ string }
	for _, file := range pkg.Files {
		for _, decl := range file.Decls {
			gd, ok := decl.(*ast.GenDecl)
			if !ok || gd.Tok != token.TYPE {
				continue
			}
			for _, spec := range gd.Specs {
				ts := spec.(*ast.TypeSpec)
				strct, ok := ts.Type.(*ast.StructType)
				if !ok || ts.Name.Name != name {
					continue
				}
				for _, f := range strct.Fields.List {
					id, ok := f.Type.(*ast.Ident)
					if !ok || !wireTypes[id.Name] {
						continue
					}
					for _, n := range f.Names {
						if n.IsExported() {
							fields = append(fields, struct{ name, typ string }{n.Name, id.Name})
						}
					}
				}
			}
		}
	}
	return fields
}

func generateRecord(w func(string, ...any), strct *ast.StructType, name, key string) int {
	// The wire fields: exported fields of wire type, in declaration
	// order; anything else is guest-only and gets no setter.
	type field struct{ name, typ string }
	var fields []field
	for _, f := range strct.Fields.List {
		id, ok := f.Type.(*ast.Ident)
		if !ok || !wireTypes[id.Name] {
			continue
		}
		for _, n := range f.Names {
			if n.IsExported() {
				fields = append(fields, field{n.Name, id.Name})
			}
		}
	}
	if len(fields) == 0 {
		fatal("kaya-gen: %s has no wire-typed exported fields", name)
	}

	w("// %sCollection declares the record collection; the struct is the", name)
	w("// schema.")
	w("func %sCollection(tx *kaya.Tx) kaya.RecordCollection[%s, %s] {", name, key, name)
	w("\treturn kaya.CollectionOf[%s, %s](tx)", key, name)
	w("}")
	w("")
	w("// %sPatch opens a named-setter patch on one entry; each setter", name)
	w("// records one update_field — a patch is recorded writes, never a")
	w("// diff.")
	w("func %sPatch(c kaya.RecordCollection[%s, %s], tx *kaya.Tx, key %s) %sPatchBuilder {", name, key, name, key, lowerFirst(name))
	w("\treturn %sPatchBuilder{c.Patch(tx, key)}", lowerFirst(name))
	w("}")
	w("")
	w("type %sPatchBuilder struct{ p kaya.RecordPatch[%s, %s] }", lowerFirst(name), key, name)
	for _, f := range fields {
		w("")
		w("func (p %sPatchBuilder) %s(v %s) %sPatchBuilder {", lowerFirst(name), f.name, f.typ, lowerFirst(name))
		w("\tp.p.Set(func(t *%s) *%s { return &t.%s }, v)", name, f.typ, f.name)
		w("\treturn p")
		w("}")
	}
	w("")
	w("// %sEach is the record template: the body runs once, authoring the", name)
	w("// blueprint with the typed row surface (exact-index tokens, no")
	w("// probes); stamping is the core's replay.")
	w("func %sEach(tx *kaya.Tx, c kaya.RecordCollection[%s, %s], body func(%sRow)) kaya.Widget {", name, key, name, lowerFirst(name))
	w("\treturn tx.ForEach(c.Collection, func(t *kaya.Tpl) {")
	w("\t\tbody(%sRow{t: t, c: c})", lowerFirst(name))
	w("\t})")
	w("}")
	w("")
	w("// The row surface: one token per wire field, and the template")
	w("// constructors that consume them.")
	w("type %sRow struct {", lowerFirst(name))
	w("\tt *kaya.Tpl")
	w("\tc kaya.RecordCollection[%s, %s]", key, name)
	w("}")
	for i, f := range fields {
		w("")
		w("func (r %sRow) %s() kaya.Field[%s] { return kaya.FieldAt[%s](%d) }", lowerFirst(name), f.name, f.typ, f.typ, i)
	}
	w("")
	w("func (r %sRow) Row(children ...kaya.Node) kaya.Node { return r.t.Row(children...) }", lowerFirst(name))
	w("")
	w("func (r %sRow) Column(children ...kaya.Node) kaya.Node { return r.t.Column(children...) }", lowerFirst(name))
	w("")
	w("func (r %sRow) Label(f kaya.Field[string]) kaya.Node { return r.c.Label(r.t, f) }", lowerFirst(name))
	w("")
	w("func (r %sRow) Checkbox(f kaya.Field[bool], onToggle func(*kaya.Tx, %s, bool)) kaya.Node {", lowerFirst(name), key)
	w("\treturn r.c.Checkbox(r.t, f, onToggle)")
	w("}")
	return len(fields)
}

func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

func fatal(f string, args ...any) {
	fmt.Fprintf(os.Stderr, f+"\n", args...)
	os.Exit(1)
}
