(* The milestone-2 scene from OCaml, on the kaya bindings: Kaya_wire
   (generated by kaya-bindgen from kaya::spec) carries the packers and
   parser, Kaya_runtime the ctypes loading and the direct-ring
   occurrence loop. What remains here is the scene declaration and the
   logic answering occurrences.

   Build the library first (cargo build), then, from a scratch dir
   holding this file plus the contents of bindings/ocaml:
       ocamlfind ocamlopt -package ctypes,ctypes-foreign,threads.posix \
           -linkpkg kaya_ml_stubs.c kaya_wire.ml kaya_runtime.ml \
           milestone2.ml -o milestone2-ocaml *)

open Kaya_wire

(* Guest-allocated ids, counted from 1 per space. *)
let sig_status = 1L
let sig_extras = 2L
let w_column = 1L
let w_step = 2L
let w_status = 3L
let w_when = 4L
let w_groups = 5L
let c_groups = 1L
let c_items = 2L
let n_banner = 1L
let n_group_col = 2L
let n_group_lbl = 3L
let n_items_for = 4L
let n_item_row = 5L
let n_item_text = 6L
let n_remove = 7L

let build_scene () =
  Kaya_runtime.submit
    [
      tx_create_signal sig_status (Str "step 0");
      tx_create_signal sig_extras (Bool false);
      tx_create_widget w_column kind_column;
      tx_create_widget w_step kind_button;
      tx_set_text w_step "step";
      tx_create_widget w_status kind_label;
      tx_bind_text w_status sig_status;
      (* When(extras): a banner label. *)
      tx_create_when w_when sig_extras;
      tx_create_widget n_banner kind_label;
      tx_set_text n_banner "extras on";
      tx_template_end ();
      (* For over groups, nesting a For over items. *)
      tx_create_collection c_groups;
      tx_create_for w_groups c_groups;
      tx_create_widget n_group_col kind_column;
      tx_create_widget n_group_lbl kind_label;
      tx_bind_text_element n_group_lbl;
      tx_add_child n_group_col n_group_lbl;
      tx_create_collection c_items;
      tx_create_for n_items_for c_items;
      tx_create_widget n_item_row kind_column;
      tx_create_widget n_item_text kind_label;
      tx_bind_text_element n_item_text;
      tx_create_widget n_remove kind_button;
      tx_set_text n_remove "remove";
      tx_add_child n_item_row n_item_text;
      tx_add_child n_item_row n_remove;
      tx_template_end ();
      tx_add_child n_group_col n_items_for;
      tx_template_end ();
      tx_add_child w_column w_step;
      tx_add_child w_column w_status;
      tx_add_child w_column w_when;
      tx_add_child w_column w_groups;
      tx_mount 0L w_column (* window 0: the default *);
    ]

let app () =
  build_scene ();
  let rec loop steps =
    match Kaya_runtime.next_click () with
    | None -> () (* shutdown *)
    | Some (id, []) when id = w_step ->
        let steps = steps + 1 in
        let changes =
          match steps with
          | 1 ->
              [
                tx_collection_insert c_groups [] (Str "g1") (Str "Work");
                tx_collection_insert c_items [ Str "g1" ] (Str "a")
                  (Str "send report");
                tx_collection_insert c_items [ Str "g1" ] (Str "b")
                  (Str "buy milk");
              ]
          | 2 ->
              [
                tx_collection_insert c_groups [] (Str "g2") (Str "Home");
                tx_collection_insert c_items [ Str "g2" ] (Str "a")
                  (Str "water plants");
                tx_collection_update c_groups [] (Str "g1") (Str "Office");
              ]
          | _ -> []
        in
        Kaya_runtime.submit
          (changes
          @ [
              tx_write_signal sig_extras (Bool (steps = 1));
              tx_write_signal sig_status (Str (Printf.sprintf "step %d" steps));
            ]);
        loop steps
    | Some (id, [ Str group; Str item ]) when id = n_remove ->
        Kaya_runtime.submit
          [
            tx_collection_remove c_items [ Str group ] (Str item);
            tx_write_signal sig_status
              (Str (Printf.sprintf "removed %s/%s" group item));
          ];
        loop steps
    | Some _ -> loop steps
  in
  loop 0

let () =
  (* Joined, not abandoned: after run returns, the core has signalled
     Shutdown, so the app loop ends and the join completes. *)
  let app_thread = Thread.create app () in
  let code = Kaya_runtime.run () in
  Thread.join app_thread;
  exit code
