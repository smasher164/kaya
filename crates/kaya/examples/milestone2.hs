{- The milestone-2 scene from Haskell, on the kaya bindings: KayaWire
   (generated by kaya-bindgen from kaya::spec) carries the packers and
   parser, KayaRuntime the FFI and the direct-ring occurrence loop. What
   remains here is the scene declaration and the logic answering
   occurrences.

   Build the library first (cargo build), then:
       ghc -threaded -O -ibindings/haskell -o milestone2-hs \
           bindings/haskell/kaya_hs_stubs.c crates/kaya/examples/milestone2.hs \
           -L target/debug -lkaya -optl-Wl,-rpath,<abs path to target/debug> -}

import Control.Concurrent (forkIO, newEmptyMVar, putMVar, takeMVar)
import Data.Word (Word64)
import System.Exit (ExitCode (..), exitSuccess, exitWith)

import KayaRuntime (kayaRun, kayaSubmit, nextClick)
import KayaWire

-- Guest-allocated ids, counted from 1 per space.
sigStatus, sigExtras :: Word64
sigStatus = 1
sigExtras = 2

wColumn, wStep, wStatus, wWhen, wGroups :: Word64
wColumn = 1
wStep = 2
wStatus = 3
wWhen = 4
wGroups = 5

cGroups, cItems :: Word64
cGroups = 1
cItems = 2

nBanner, nGroupCol, nGroupLbl, nItemsFor, nItemRow, nItemText, nRemove :: Word64
nBanner = 1
nGroupCol = 2
nGroupLbl = 3
nItemsFor = 4
nItemRow = 5
nItemText = 6
nRemove = 7

buildScene :: IO ()
buildScene =
  kayaSubmit
    [ txCreateSignal sigStatus (VStr "step 0"),
      txCreateSignal sigExtras (VBool False),
      txCreateWidget wColumn kindColumn,
      txCreateWidget wStep kindButton,
      txSetText wStep "step",
      txCreateWidget wStatus kindLabel,
      txBindText wStatus sigStatus,
      -- When(extras): a banner label.
      txCreateWhen wWhen sigExtras,
      txCreateWidget nBanner kindLabel,
      txSetText nBanner "extras on",
      txTemplateEnd,
      -- For over groups, nesting a For over items.
      txCreateCollection cGroups,
      txCreateFor wGroups cGroups,
      txCreateWidget nGroupCol kindColumn,
      txCreateWidget nGroupLbl kindLabel,
      txBindTextElement nGroupLbl 0,
      txAddChild nGroupCol nGroupLbl,
      txCreateCollection cItems,
      txCreateFor nItemsFor cItems,
      txCreateWidget nItemRow kindColumn,
      txCreateWidget nItemText kindLabel,
      txBindTextElement nItemText 0,
      txCreateWidget nRemove kindButton,
      txSetText nRemove "remove",
      txAddChild nItemRow nItemText,
      txAddChild nItemRow nRemove,
      txTemplateEnd,
      txAddChild nGroupCol nItemsFor,
      txTemplateEnd,
      txAddChild wColumn wStep,
      txAddChild wColumn wStatus,
      txAddChild wColumn wWhen,
      txAddChild wColumn wGroups,
      txMount 0 wColumn -- window 0: the default
    ]

app :: IO ()
app = do
  buildScene
  let loop steps = do
        click <- nextClick
        case click of
          Nothing -> return () -- shutdown
          Just (ident, [])
            | ident == wStep -> do
                let n = steps + 1
                    changes = case n of
                      1 ->
                        [ txCollectionInsert cGroups [] (VStr "g1") (VStr "Work"),
                          txCollectionInsert cItems [VStr "g1"] (VStr "a")
                            (VStr "send report"),
                          txCollectionInsert cItems [VStr "g1"] (VStr "b")
                            (VStr "buy milk")
                        ]
                      2 ->
                        [ txCollectionInsert cGroups [] (VStr "g2") (VStr "Home"),
                          txCollectionInsert cItems [VStr "g2"] (VStr "a")
                            (VStr "water plants"),
                          txCollectionUpdate cGroups [] (VStr "g1") (VStr "Office")
                        ]
                      _ -> []
                kayaSubmit
                  ( changes
                      ++ [ txWriteSignal sigExtras (VBool (n == 1)),
                           txWriteSignal sigStatus (VStr ("step " ++ show n))
                         ]
                  )
                loop n
          Just (ident, [VStr group, VStr item])
            | ident == nRemove -> do
                kayaSubmit
                  [ txCollectionRemove cItems [VStr group] (VStr item),
                    txWriteSignal sigStatus
                      (VStr ("removed " ++ group ++ "/" ++ item))
                  ]
                loop steps
          Just _ -> loop steps
  loop 0

main :: IO ()
main = do
  -- Joined, not abandoned: after kayaRun returns, the core has
  -- signalled Shutdown, so the app loop ends and the join completes.
  done <- newEmptyMVar
  _ <- forkIO (app >> putMVar done ())
  code <- kayaRun -- takes over the main thread until the app exits
  takeMVar done
  if code == 0 then exitSuccess else exitWith (ExitFailure (fromIntegral code))
