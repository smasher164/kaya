//! Python emitter: one self-contained module of constants, packers, and
//! the occurrence parser.

use kaya::spec::{FieldTy, ProtocolSpec};

use crate::{Ctx, PropKind, prop_variants, record_params, window_prop_variants};

/// Names spec identifiers must avoid: this emitter's helpers, plus
/// Python's keywords and builtins a parameter would shadow.
pub const RESERVED: &[&str] = &[
    "_enc", "record", "parse_value", "parse_occurrence", "struct", "BlobHandle",
    "and", "as", "assert", "async", "await", "break", "class", "continue", "def", "del",
    "elif", "else", "except", "finally", "for", "from", "global", "if", "import", "in",
    "is", "lambda", "nonlocal", "not", "or", "pass", "raise", "return", "try", "while",
    "with", "yield", "type", "bytes", "len", "int", "str", "float", "bool",
];

pub fn emit(spec: &ProtocolSpec) -> String {
    let mut c = Ctx { out: String::new() };
    c.line("\"\"\"kaya wire vocabulary. Generated by kaya-bindgen from kaya::spec — do not edit.");
    c.line("");
    c.line("Records are {u32 size, u16 kind, u16 flags}, body, padded to 8, little-endian.");
    c.line("Values are {u32 type, u32 len, payload padded to 8}; paths are");
    c.line("{u32 count, u32 reserved, count values}. Keys and values here accept");
    c.line("Python bool, int, float, str, and BlobHandle, mapped to the kaya");
    c.line("value types.");
    c.line("\"\"\"");
    c.line("");
    c.line("import struct");
    c.line("");

    c.line(&format!("# SPEC_HASH: {}.", "the protocol fingerprint; the runtime asserts the loaded core agrees"));
    c.line(&format!("SPEC_HASH = 0x{:016x}", crate::spec_hash()));
    c.line("");
    for e in spec.enums {
        for (name, value) in e.variants {
            c.line(&format!(
                "{}_{} = {}",
                e.name.to_uppercase(),
                name.to_uppercase(),
                value
            ));
        }
    }
    c.line("");
    for (channel, records) in [("TX", spec.tx), ("APPLY", spec.apply), ("OCC", spec.occurrence)] {
        for r in records {
            c.line(&format!("{channel}_{} = {}", r.name.to_uppercase(), r.kind));
        }
    }

    c.line("");
    c.line("");
    c.line("def _pad(b):");
    c.line("    return b + b\"\\0\" * (-len(b) % 8)");
    c.line("");
    c.line("");
    c.line("class BlobHandle:");
    c.line("    \"\"\"A blob value at the wire tier: the u64 handle from");
    c.line("    kaya_blob_register, consumed by the next submit. The bytes never");
    c.line("    ride the record stream; the wrapper distinguishes a blob from a");
    c.line("    plain int in the type-dispatched encoders.\"\"\"");
    c.line("");
    c.line("    __slots__ = (\"handle\",)");
    c.line("");
    c.line("    def __init__(self, handle):");
    c.line("        self.handle = handle");
    c.line("");
    c.line("");
    c.line("class _enc:");
    c.line("    \"\"\"Encoders, namespaced so no generated parameter can shadow them.\"\"\"");
    c.line("");
    c.line("    @staticmethod");
    c.line("    def value(v):");
    c.line("        \"\"\"Encode a Python scalar as a kaya value.\"\"\"");
    c.line("        if isinstance(v, bool):");
    c.line("            return _pad(struct.pack(\"<II\", VALUE_BOOL, 1) + bytes([v]))");
    c.line("        if isinstance(v, int):");
    c.line("            return struct.pack(\"<IIq\", VALUE_I64, 8, v)");
    c.line("        if isinstance(v, float):");
    c.line("            return struct.pack(\"<IId\", VALUE_F64, 8, v)");
    c.line("        if isinstance(v, BlobHandle):");
    c.line("            return struct.pack(\"<IIQ\", VALUE_BLOB, 8, v.handle)");
    c.line("        utf8 = v.encode()");
    c.line("        return _pad(struct.pack(\"<II\", VALUE_STR, len(utf8)) + utf8)");
    c.line("");
    c.line("");
    c.line("    @staticmethod");
    c.line("    def values(vals):");
    c.line("        \"\"\"Encode a counted value sequence: a key path or a record.\"\"\"");
    c.line("        return struct.pack(\"<II\", len(vals), 0) + b\"\".join(_enc.value(v) for v in vals)");
    c.line("");
    c.line("");
    c.line("    @staticmethod");
    c.line("    def variant_schemas(variants):");
    c.line("        \"\"\"Encode a collection's element sum: per variant, a counted list of VALUE_* tags. A record collection is the one-variant case.\"\"\"");
    c.line("        body = struct.pack(\"<II\", len(variants), 0)");
    c.line("        for schema in variants:");
    c.line("            body += struct.pack(\"<I\", len(schema)) + b\"\".join(struct.pack(\"<I\", t) for t in schema)");
    c.line("        return _pad(body)");
    c.line("");
    c.line("");
    c.line("def record(kind, body):");
    c.line("    \"\"\"Frame one record.\"\"\"");
    c.line("    body = _pad(body)");
    c.line("    return struct.pack(\"<IHH\", 8 + len(body), kind, 0) + body");
    c.line("");

    // One packer per fixed-layout tx record; set_property gets one
    // helper per source arm.
    for r in spec.tx {
        if r.name == "set_property" || r.name == "set_window_prop" {
            continue;
        }
        let params = record_params(r);
        let names: Vec<String> = params.iter().map(|f| f.name.to_string()).collect();
        c.line("");
        c.line(&format!("def tx_{}({}):", r.name, names.join(", ")));
        c.line(&format!("    \"\"\"{}\"\"\"", r.doc.replace('\n', " ")));
        let mut parts: Vec<String> = Vec::new();
        for f in r.fields {
            parts.push(match f.ty {
                FieldTy::U32 if f.name == "reserved" => "struct.pack(\"<I\", 0)".into(),
                FieldTy::U32 => format!("struct.pack(\"<I\", {})", f.name),
                FieldTy::U64 => format!("struct.pack(\"<Q\", {})", f.name),
                FieldTy::Value => format!("_enc.value({})", f.name),
                FieldTy::Values => format!("_enc.values({})", f.name),
                FieldTy::VariantSchemas => format!("_enc.variant_schemas({})", f.name),
            });
        }
        let body = if parts.is_empty() {
            "b\"\"".to_string()
        } else {
            parts.join(" + ")
        };
        c.line(&format!("    return record(TX_{}, {})", r.name.to_uppercase(), body));
    }

    // The set_property trios, one per property: spec-driven so new
    // props reach every binding without emitter edits. Python's value
    // encoder is type-generic, so PropKind only shapes the docstring.
    for (prop, _, kind) in prop_variants(spec) {
        let up = prop.to_uppercase();
        // Blob setters speak the wire tier: the parameter is the u64
        // handle from kaya_blob_register (see BlobHandle), not bytes.
        let (param, ty, expr) = match kind {
            PropKind::Str => (*prop, "str", format!("_enc.value({prop})")),
            PropKind::Bool => (*prop, "bool", format!("_enc.value({prop})")),
            PropKind::F64 => (*prop, "float", format!("_enc.value({prop})")),
            PropKind::Enum(_) => (*prop, "int", format!("_enc.value(int({prop}))")),
            PropKind::Blob => (
                "handle",
                "a kaya_blob_register handle, consumed by the next submit",
                "_enc.value(BlobHandle(handle))".to_string(),
            ),
        };
        c.line("");
        c.line("");
        c.line(&format!("def tx_set_{prop}(widget_id, {param}):"));
        c.line(&format!("    \"\"\"set_property with a constant {prop} value ({ty}).\"\"\""));
        c.line(&format!("    return record(TX_SET_PROPERTY, struct.pack(\"<QII\", widget_id, PROP_{up}, SOURCE_CONST) + {expr})"));
        c.line("");
        c.line("");
        c.line(&format!("def tx_bind_{prop}(widget_id, signal_id):"));
        c.line(&format!("    \"\"\"set_property with a signal-bound {prop} value.\"\"\""));
        c.line(&format!("    return record(TX_SET_PROPERTY, struct.pack(\"<QIIQ\", widget_id, PROP_{up}, SOURCE_SIGNAL, signal_id))"));
        c.line("");
        c.line("");
        c.line(&format!("def tx_bind_{prop}_element(widget_id, level=0, field=0):"));
        c.line(&format!("    \"\"\"set_property bound to one field of the element of the enclosing For, `level` Fors up.\"\"\""));
        c.line(&format!("    return record(TX_SET_PROPERTY, struct.pack(\"<QIIII\", widget_id, PROP_{up}, SOURCE_ELEMENT, level, field))"));
    }

    // The window-prop duos (const + signal — element sources are
    // rejected by the wire; windows are not collection elements).
    // Window 0, the primary surface, until aux windows land.
    for (prop, _, kind) in window_prop_variants(spec) {
        let up = prop.to_uppercase();
        let (ty, expr) = match kind {
            PropKind::Str => ("str", format!("_enc.value({prop})")),
            PropKind::F64 => ("float", format!("_enc.value({prop})")),
            PropKind::Bool => ("bool", format!("_enc.value({prop})")),
            other => unreachable!("no window prop carries {other:?}"),
        };
        c.line("");
        c.line("");
        c.line(&format!("def tx_set_window_{prop}(window, {prop}):"));
        c.line(&format!("    \"\"\"set_window_prop with a constant {prop} value ({ty}); window 0, the primary surface.\"\"\""));
        c.line(&format!("    return record(TX_SET_WINDOW_PROP, struct.pack(\"<QII\", window, WPROP_{up}, SOURCE_CONST) + {expr})"));
        c.line("");
        c.line("");
        c.line(&format!("def tx_bind_window_{prop}(window, signal_id):"));
        c.line(&format!("    \"\"\"set_window_prop with a signal-bound {prop} value; window 0, the primary surface.\"\"\""));
        c.line(&format!("    return record(TX_SET_WINDOW_PROP, struct.pack(\"<QIIQ\", window, WPROP_{up}, SOURCE_SIGNAL, signal_id))"));
    }
    c.line("");
    c.line("");
    c.line("def parse_value(buf, at):");
    c.line("    \"\"\"Decode one value; returns (python value, next offset).\"\"\"");
    c.line("    vtype, vlen = struct.unpack_from(\"<II\", buf, at)");
    c.line("    payload = buf[at + 8:at + 8 + vlen]");
    c.line("    end = at + 8 + ((vlen + 7) & ~7)");
    c.line("    if vtype == VALUE_BOOL:");
    c.line("        return payload[0] != 0, end");
    c.line("    if vtype == VALUE_I64:");
    c.line("        return struct.unpack(\"<q\", payload)[0], end");
    c.line("    if vtype == VALUE_F64:");
    c.line("        return struct.unpack(\"<d\", payload)[0], end");
    c.line("    if vtype == VALUE_STR:");
    c.line("        return payload.decode(), end");
    c.line("    if vtype == VALUE_BLOB:");
    c.line("        return BlobHandle(struct.unpack(\"<Q\", payload)[0]), end");
    c.line("    raise ValueError(f\"unknown value type {vtype}\")");
    c.line("");
    c.line("");
    c.line("def parse_occurrence(buf):");
    c.line("    \"\"\"Decode one occurrence record (header included).");
    c.line("");
    c.line("    Returns (kind, id, keys, payload). keys is [] when id is a");
    c.line("    widget id, else id is a template node id and keys is the");
    c.line("    copy's key path. text is the entry's new content for");
    c.line("    OCC_TEXT_CHANGED, the new state for OCC_TOGGLED, the new");
    c.line("    value for OCC_VALUE_CHANGED, None otherwise.");
    // (kind lists below derive from the spec's Record::payload.)
    c.line("    \"\"\"");
    c.line("    _size, kind, _flags = struct.unpack_from(\"<IHH\", buf, 0)");
    let accepted = crate::occurrence_names(spec)
        .iter()
        .map(|n| format!("OCC_{}", n.to_uppercase()))
        .collect::<Vec<_>>()
        .join(", ");
    c.line(&format!("    if kind not in ({accepted}):"));
    c.line("        return kind, None, [], None");
        c.line("    if kind in (OCC_CLOSE_REQUESTED, OCC_WINDOW_CLOSED):");
    c.line("        # Window lifecycle records carry the window id alone —");
    c.line("        # no key path, no payload.");
    c.line("        (window_id,) = struct.unpack_from(\"<Q\", buf, 8)");
    c.line("        return kind, window_id, [], None");
c.line("    ident, path_len = struct.unpack_from(\"<QI\", buf, 8)");
    c.line("    keys = []");
    c.line("    at = 24");
    c.line("    for _ in range(path_len):");
    c.line("        key, at = parse_value(buf, at)");
    c.line("        keys.append(key)");
    c.line("    payload = None");
    let with_payload = crate::payload_occurrence_names(spec)
        .iter()
        .map(|n| format!("OCC_{}", n.to_uppercase()))
        .collect::<Vec<_>>()
        .join(", ");
    c.line(&format!("    if kind in ({with_payload},):"));
    c.line("        payload, at = parse_value(buf, at)");
    c.line("    return kind, ident, keys, payload");

    c.out
}
