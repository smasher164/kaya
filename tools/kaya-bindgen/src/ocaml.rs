//! OCaml emitter: module Kaya_wire — a value variant, constants,
//! Buffer-based packers returning packed record strings, and the
//! occurrence parser over a byte accessor (transport-agnostic: the
//! runtime bakes in the Bigarray). Module scope namespaces everything;
//! OCaml's separate type and value namespaces mean a parameter named
//! `value` coexists with the `value` type.

use kaya::spec::{FieldTy, ProtocolSpec, Record};

use crate::{Ctx, prop_variants, record_params, window_prop_variants};

pub const RESERVED: &[&str] = &[
    "encode_value", "encode_values", "encode_variant_schemas", "finish", "parse_value", "parse_occurrence",
    "and", "as", "assert", "begin", "class", "constraint", "do", "done", "downto", "else",
    "end", "exception", "external", "false", "for", "fun", "function", "functor", "if", "in",
    "include", "inherit", "initializer", "lazy", "let", "match", "method", "module", "mutable",
    "new", "nonrec", "object", "of", "open", "or", "private", "rec", "sig", "struct", "then",
    "to", "true", "try", "type", "val", "virtual", "when", "while", "with",
];

pub fn emit(spec: &ProtocolSpec) -> String {
    let mut c = Ctx { out: String::new() };
    c.line("(* kaya wire vocabulary. Generated by kaya-bindgen from kaya::spec — do not edit.");
    c.line("");
    c.line("   Records are {u32 size, u16 kind, u16 flags}, body, padded to 8,");
    c.line("   little-endian; each tx_* returns one packed record, and a");
    c.line("   transaction is their concatenation. *)");
    c.line("");
    c.line("(* Blob carries the u64 handle from kaya_blob_register (as int64),");
    c.line("   consumed by the next submit; the bytes never ride the record");
    c.line("   stream. *)");
    c.line("type value =");
    c.line("  | Bool of bool");
    c.line("  | I64 of int64");
    c.line("  | F64 of float");
    c.line("  | Str of string");
    c.line("  | Blob of int64");
    c.line("");
    c.line(&format!("(* spec_hash: {}. *)", "the protocol fingerprint; the runtime asserts the loaded core agrees"));
    c.line(&format!("let spec_hash = 0x{:016x}L", crate::spec_hash()));
    c.line("");
    for e in spec.enums {
        for (name, value) in e.variants {
            c.line(&format!("let {}_{} = {}", e.name, name, value));
        }
    }
    for (channel, records) in [("tx", spec.tx), ("apply", spec.apply), ("occ", spec.occurrence)] {
        for r in records {
            c.line(&format!("let {channel}_kind_{} = {}", r.name, r.kind));
        }
    }
    c.line("");
    c.line("let pad8 b =");
    c.line("  while Buffer.length b mod 8 <> 0 do");
    c.line("    Buffer.add_char b '\\000'");
    c.line("  done");
    c.line("");
    c.line("(* Values self-pad to 8: they concatenate inside record bodies. *)");
    c.line("let encode_value b v =");
    c.line("  (match v with");
    c.line("  | Bool x ->");
    c.line("      Buffer.add_int32_le b (Int32.of_int value_bool);");
    c.line("      Buffer.add_int32_le b 1l;");
    c.line("      Buffer.add_char b (if x then '\\001' else '\\000')");
    c.line("  | I64 x ->");
    c.line("      Buffer.add_int32_le b (Int32.of_int value_i64);");
    c.line("      Buffer.add_int32_le b 8l;");
    c.line("      Buffer.add_int64_le b x");
    c.line("  | F64 x ->");
    c.line("      Buffer.add_int32_le b (Int32.of_int value_f64);");
    c.line("      Buffer.add_int32_le b 8l;");
    c.line("      Buffer.add_int64_le b (Int64.bits_of_float x)");
    c.line("  | Str x ->");
    c.line("      Buffer.add_int32_le b (Int32.of_int value_str);");
    c.line("      Buffer.add_int32_le b (Int32.of_int (String.length x));");
    c.line("      Buffer.add_string b x");
    c.line("  | Blob x ->");
    c.line("      Buffer.add_int32_le b (Int32.of_int value_blob);");
    c.line("      Buffer.add_int32_le b 8l;");
    c.line("      Buffer.add_int64_le b x);");
    c.line("  pad8 b");
    c.line("");
    c.line("(* A key path: {u32 count, u32 reserved, count values}. *)");
    c.line("(* A counted value sequence: a key path or a record. *)");
    c.line("let encode_values b vals =");
    c.line("  Buffer.add_int32_le b (Int32.of_int (List.length vals));");
    c.line("  Buffer.add_int32_le b 0l;");
    c.line("  List.iter (encode_value b) vals");
    c.line("");
    c.line("(* A collection's element sum: per variant, counted value-type tags,");
    c.line("   padded to 8. A record collection is the one-variant case. *)");
    c.line("let encode_variant_schemas b variants =");
    c.line("  Buffer.add_int32_le b (Int32.of_int (List.length variants));");
    c.line("  Buffer.add_int32_le b 0l;");
    c.line("  List.iter");
    c.line("    (fun schema ->");
    c.line("      Buffer.add_int32_le b (Int32.of_int (List.length schema));");
    c.line("      List.iter (fun t -> Buffer.add_int32_le b (Int32.of_int t)) schema)");
    c.line("    variants;");
    c.line("  pad8 b");
    c.line("");
    c.line("let finish kind fill =");
    c.line("  let b = Buffer.create 64 in");
    c.line("  Buffer.add_int32_le b 0l;");
    c.line("  Buffer.add_int16_le b kind;");
    c.line("  Buffer.add_int16_le b 0;");
    c.line("  fill b;");
    c.line("  pad8 b;");
    c.line("  let s = Bytes.of_string (Buffer.contents b) in");
    c.line("  Bytes.set_int32_le s 0 (Int32.of_int (Bytes.length s));");
    c.line("  Bytes.to_string s");

    for r in spec.tx {
        if r.name == "set_property" || r.name == "set_window_prop" {
            continue;
        }
        emit_packer(&mut c, r);
    }

    // The set_property arms, one trio per property: spec-driven so new
    // props reach every binding without emitter edits.
    for (prop, _, kind) in prop_variants(spec) {
        // Blob setters take the u64 kaya_blob_register handle (see
        // Blob), so the parameter says so.
        let (ctor, param) = match kind {
            crate::PropKind::Str => ("Str", *prop),
            crate::PropKind::Bool => ("Bool", *prop),
            crate::PropKind::F64 => ("F64", *prop),
            crate::PropKind::Blob => ("Blob", "handle"),
            crate::PropKind::Enum(_) => ("I64", *prop),
        };
        c.line("");
        c.line(&format!("(* set_property with a constant {prop} value. *)"));
        c.line(&format!("let tx_set_{prop} widget_id {param} ="));
        c.line("  finish tx_kind_set_property (fun b ->");
        c.line("      Buffer.add_int64_le b widget_id;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int prop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_const);");
        c.line(&format!("      encode_value b ({ctor} {param}))"));
        c.line("");
        c.line(&format!("(* set_property with a signal-bound {prop} value. *)"));
        c.line(&format!("let tx_bind_{prop} widget_id signal_id ="));
        c.line("  finish tx_kind_set_property (fun b ->");
        c.line("      Buffer.add_int64_le b widget_id;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int prop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_signal);");
        c.line("      Buffer.add_int64_le b signal_id)");
        c.line("");
        c.line("(* set_property bound to one field of the element of the enclosing");
        c.line("   For, `level` Fors up (0 = nearest; field 0 for a scalar). *)");
        c.line(&format!("let tx_bind_{prop}_element ?(level = 0) ?(field = 0) widget_id ="));
        c.line("  finish tx_kind_set_property (fun b ->");
        c.line("      Buffer.add_int64_le b widget_id;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int prop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_element);");
        c.line("      Buffer.add_int32_le b (Int32.of_int level);");
        c.line("      Buffer.add_int32_le b (Int32.of_int field))");
    }

    // The window-prop duos (const + signal — element sources are
    // rejected by the wire). Window 0, the primary surface, until aux
    // windows land.
    for (prop, _, kind) in window_prop_variants(spec) {
        let (ctor, param) = match kind {
            crate::PropKind::Str => ("Str", *prop),
            crate::PropKind::F64 => ("F64", *prop),
            crate::PropKind::Bool => ("Bool", *prop),
            crate::PropKind::Enum(_) => ("I64", *prop),
            other => unreachable!("no window prop carries {other:?}"),
        };
        c.line("");
        c.line(&format!("(* set_window_prop with a constant {prop} value (window 0, the primary surface). *)"));
        c.line(&format!("let tx_set_window_{prop} window {param} ="));
        c.line("  finish tx_kind_set_window_prop (fun b ->");
        c.line("      Buffer.add_int64_le b window;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int wprop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_const);");
        c.line(&format!("      encode_value b ({ctor} {param}))"));
        c.line("");
        c.line(&format!("(* set_window_prop with a signal-bound {prop} value (window 0, the primary surface). *)"));
        c.line(&format!("let tx_bind_window_{prop} window signal_id ="));
        c.line("  finish tx_kind_set_window_prop (fun b ->");
        c.line("      Buffer.add_int64_le b window;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int wprop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_signal);");
        c.line("      Buffer.add_int64_le b signal_id)");
    }

    // The entry-prop duos (const + signal), the window shape on the
    // navigation-entry table (DESIGN.md, Navigation).
    for (prop, _, kind) in crate::entry_prop_variants(spec) {
        let (ctor, param) = match kind {
            crate::PropKind::Str => ("Str", *prop),
            crate::PropKind::Bool => ("Bool", *prop),
            other => unreachable!("no entry prop carries {other:?}"),
        };
        c.line("");
        c.line(&format!("(* set_entry_prop with a constant {prop} value. *)"));
        c.line(&format!("let tx_set_entry_{prop} entry {param} ="));
        c.line("  finish tx_kind_set_entry_prop (fun b ->");
        c.line("      Buffer.add_int64_le b entry;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int eprop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_const);");
        c.line(&format!("      encode_value b ({ctor} {param}))"));
        c.line("");
        c.line(&format!("(* set_entry_prop with a signal-bound {prop} value. *)"));
        c.line(&format!("let tx_bind_entry_{prop} entry signal_id ="));
        c.line("  finish tx_kind_set_entry_prop (fun b ->");
        c.line("      Buffer.add_int64_le b entry;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int eprop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_signal);");
        c.line("      Buffer.add_int64_le b signal_id)");
    }

    // The section-prop duos (const + signal), the entry shape on the
    // section table; icon rides the blob channel (DESIGN.md, Sections).
    for (prop, _, kind) in crate::section_prop_variants(spec) {
        let (ctor, param) = match kind {
            crate::PropKind::Str => ("Str", *prop),
            crate::PropKind::Blob => ("Blob", "handle"),
            other => unreachable!("no section prop carries {other:?}"),
        };
        c.line("");
        c.line(&format!("(* set_section_prop with a constant {prop} value. *)"));
        c.line(&format!("let tx_set_section_{prop} section {param} ="));
        c.line("  finish tx_kind_set_section_prop (fun b ->");
        c.line("      Buffer.add_int64_le b section;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int sprop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_const);");
        c.line(&format!("      encode_value b ({ctor} {param}))"));
        c.line("");
        c.line(&format!("(* set_section_prop with a signal-bound {prop} value. *)"));
        c.line(&format!("let tx_bind_section_{prop} section signal_id ="));
        c.line("  finish tx_kind_set_section_prop (fun b ->");
        c.line("      Buffer.add_int64_le b section;");
        c.line(&format!("      Buffer.add_int32_le b (Int32.of_int sprop_{prop});"));
        c.line("      Buffer.add_int32_le b (Int32.of_int source_signal);");
        c.line("      Buffer.add_int64_le b signal_id)");
    }
    c.line("");
    c.line("(* Reads assembled from a byte accessor (absolute offset -> byte);");
    c.line("   kaya v1 targets are all little-endian. *)");
    c.line("let u16_at byte i = byte i lor (byte (i + 1) lsl 8)");
    c.line("let u32_at byte i = u16_at byte i lor (u16_at byte (i + 2) lsl 16)");
    c.line("");
    c.line("(* Decode one value at offset [at]; returns (value, next offset). *)");
    c.line("let parse_value byte at =");
    c.line("  let vtype = u32_at byte at in");
    c.line("  let vlen = u32_at byte (at + 4) in");
    c.line("  let next = at + 8 + ((vlen + 7) land lnot 7) in");
    c.line("  let v =");
    c.line("    if vtype = value_bool then Bool (byte (at + 8) <> 0)");
    c.line("    else if vtype = value_i64 || vtype = value_f64 || vtype = value_blob");
    c.line("    then begin");
    c.line("      let n = ref 0L in");
    c.line("      for i = 7 downto 0 do");
    c.line("        n := Int64.logor (Int64.shift_left !n 8)");
    c.line("               (Int64.of_int (byte (at + 8 + i)))");
    c.line("      done;");
    c.line("      if vtype = value_i64 then I64 !n");
    c.line("      else if vtype = value_blob then Blob !n");
    c.line("      else F64 (Int64.float_of_bits !n)");
    c.line("    end");
    c.line("    else Str (String.init vlen (fun i -> Char.chr (byte (at + 8 + i))))");
    c.line("  in");
    c.line("  (v, next)");
    c.line("");
    c.line("(* Decode one occurrence record (header included) through the byte");
    c.line("   accessor. Some (kind, id, keys, payload) — keys is [] when id is");
    c.line("   a widget id, else id is a template node id and keys is the copy's");
    c.line("   key path; payload is Some for text_changed/toggled/value_changed");
    c.line("   (a Str, Bool, or F64");
    c.line("   value), None for clicks. None for pad/unknown kinds. *)");
    c.line("let parse_occurrence byte =");
    c.line("  let kind = u16_at byte 4 in");
    let accepted = crate::occurrence_names(spec)
        .iter()
        .map(|n| format!("kind <> occ_kind_{n}"))
        .collect::<Vec<_>>()
        .join(" && ");
    c.line(&format!("  if {accepted} then None"));
    c.line("  else begin");
    c.line("    (* ids are guest-allocated and small; the low u32 is the story. *)");
    c.line("    let id = u32_at byte 8 in");
    c.line("    if kind = occ_kind_alert_result");
    c.line("    then");
    c.line("      (* The alert's one answer: id + u32 choice (the alert_choice values). *)");
    c.line("      Some (kind, Int64.of_int id, [], Some (I64 (Int64.of_int (u32_at byte 16))))");
    let id_only = crate::id_only_occurrence_names(spec)
        .iter()
        .map(|n| format!("kind = occ_kind_{n}"))
        .collect::<Vec<_>>()
        .join(" || ");
    c.line("    (* Surface lifecycle records carry the surface id alone");
    c.line("       ( derived from the record shapes ). *)");
    c.line(&format!("    else if {id_only}"));
    c.line("    then Some (kind, Int64.of_int id, [], None)");
    let id_pair = crate::id_pair_occurrence_names(spec)
        .iter()
        .map(|n| format!("kind = occ_kind_{n}"))
        .collect::<Vec<_>>()
        .join(" || ");
    if !id_pair.is_empty() {
        c.line("    (* Surface-pair records (window, section): the SECOND id");
        c.line("       keys the handler; the first rides as the payload. *)");
        c.line(&format!("    else if {id_pair}"));
        c.line("    then");
        c.line("      Some");
        c.line("        ( kind,");
        c.line("          Int64.of_int (u32_at byte 16),");
        c.line("          [],");
        c.line("          Some (I64 (Int64.of_int id)) )");
    }
    c.line("    else begin");
    c.line("    let path_len = u32_at byte 16 in");
    c.line("    let keys = ref [] in");
    c.line("    let at = ref 24 in");
    c.line("    for _ = 1 to path_len do");
    c.line("      let v, next = parse_value byte !at in");
    c.line("      keys := v :: !keys;");
    c.line("      at := next");
    c.line("    done;");
    c.line("    let payload =");
    let with_payload = crate::payload_occurrence_names(spec)
        .iter()
        .map(|n| format!("kind = occ_kind_{n}"))
        .collect::<Vec<_>>()
        .join(" || ");
    c.line(&format!("      if {with_payload} then"));
    c.line("        Some (fst (parse_value byte !at))");
    c.line("      else None");
    c.line("    in");
    c.line("    Some (kind, Int64.of_int id, List.rev !keys, payload)");
    c.line("    end");
    c.line("  end");
    c.out
}

fn emit_packer(c: &mut Ctx, r: &Record) {
    let params = record_params(r);
    let args: Vec<&str> = params.iter().map(|f| f.name).collect();
    let args = if args.is_empty() {
        "()".to_string()
    } else {
        args.join(" ")
    };
    c.line("");
    // OCaml comments nest and `*)` TERMINATES one: a spec doc that
    // ever contains `*)` (or opens `(*`) would break the generated
    // file at a distance — defuse both delimiters structurally.
    c.line(&format!(
        "(* {} *)",
        r.doc.replace('\n', " ").replace("*)", "* )").replace("(*", "( *")
    ));
    c.line(&format!("let tx_{} {} =", r.name, args));
    c.line(&format!("  finish tx_kind_{} (fun b ->", r.name));
    let mut lines: Vec<String> = Vec::new();
    for f in r.fields {
        lines.push(match f.ty {
            FieldTy::U32 if f.name == "reserved" => "      Buffer.add_int32_le b 0l".into(),
            FieldTy::U32 => format!("      Buffer.add_int32_le b (Int32.of_int {})", f.name),
            FieldTy::U64 => format!("      Buffer.add_int64_le b {}", f.name),
            FieldTy::Value => format!("      encode_value b {}", f.name),
            FieldTy::Values => format!("      encode_values b {}", f.name),
            FieldTy::VariantSchemas => format!("      encode_variant_schemas b {}", f.name),
        });
    }
    if lines.is_empty() {
        lines.push("      ignore b".into());
    }
    c.line(&format!("{})", lines.join(";\n")));
}
