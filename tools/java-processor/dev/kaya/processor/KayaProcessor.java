package dev.kaya.processor;

import java.io.IOException;
import java.io.Writer;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.List;
import java.util.Set;
import javax.annotation.processing.AbstractProcessor;
import javax.annotation.processing.RoundEnvironment;
import javax.annotation.processing.SupportedAnnotationTypes;
import javax.annotation.processing.SupportedOptions;
import javax.annotation.processing.SupportedSourceVersion;
import javax.lang.model.SourceVersion;
import javax.lang.model.element.Element;
import javax.lang.model.element.ElementKind;
import javax.lang.model.element.TypeElement;
import javax.lang.model.type.TypeMirror;
import javax.tools.Diagnostic;

import dev.kaya.KayaGen;

/**
 * The Java arm of the generator family. Reads a {@code @KayaGen}
 * declaration — the declaration is the schema, nothing restated — and
 * writes {@code <Type>Kaya.java} beside it ({@code -Akaya.out=<source
 * root>}). The declaration's shape decides what is generated, the one
 * KayaGen story every language tells: a sealed interface (a sum — the
 * permitted records, in permits-clause order, are the constructors)
 * gets the collection factory and the staged-builder eliminator, where
 * each stage's return type offers exactly the next constructor's arm
 * and only the final arm yields the widget — the Immutables idiom for
 * named-and-required, making template totality a compile error, with
 * the scene checking it again. A record gets the collection factory,
 * exact-index field tokens, and a named-setter patch (each set records
 * one update_field — a patch is recorded writes, never a diff).
 * Generated files are checked in; tools/gen-guests.sh regenerates and
 * diffs.
 */
@SupportedAnnotationTypes("dev.kaya.KayaGen")
@SupportedSourceVersion(SourceVersion.RELEASE_17)
@SupportedOptions("kaya.out")
public final class KayaProcessor extends AbstractProcessor {
    @Override
    public boolean process(Set<? extends TypeElement> annotations, RoundEnvironment round) {
        String out = processingEnv.getOptions().get("kaya.out");
        for (Element element : round.getElementsAnnotatedWith(KayaGen.class)) {
            boolean sum = element instanceof TypeElement type
                    && element.getKind() == ElementKind.INTERFACE
                    && !type.getPermittedSubclasses().isEmpty();
            boolean record = element.getKind() == ElementKind.RECORD;
            if (!sum && !record) {
                processingEnv.getMessager().printMessage(Diagnostic.Kind.ERROR,
                        "kaya: @KayaGen goes on a sealed interface with a permits clause"
                                + " (a sum) or a record", element);
                continue;
            }
            if (out == null) {
                processingEnv.getMessager().printMessage(Diagnostic.Kind.ERROR,
                        "kaya: pass -Akaya.out=<source root> (see tools/gen-guests.sh)", element);
                continue;
            }
            try {
                if (sum) {
                    generateSum((TypeElement) element, out);
                } else {
                    generateRecord((TypeElement) element, out);
                }
            } catch (IOException e) {
                processingEnv.getMessager().printMessage(Diagnostic.Kind.ERROR,
                        "kaya: " + e, element);
            }
        }
        return false;
    }

    private void generateSum(TypeElement sum, String out) throws IOException {
        String key = sum.getAnnotation(KayaGen.class).key();
        String pkg = processingEnv.getElementUtils().getPackageOf(sum)
                .getQualifiedName().toString();
        String sumRef = qualifiedInPackage(sum, pkg);
        String simple = sum.getSimpleName().toString();
        List<? extends TypeMirror> permitted = sum.getPermittedSubclasses();

        StringBuilder b = new StringBuilder();
        w(b, "package %s;", pkg);
        w(b, "");
        w(b, "// Code generated by the kaya annotation processor; DO NOT EDIT.");
        w(b, "// Regenerate with tools/gen-guests.sh (which also checks freshness).");
        w(b, "");
        w(b, "import dev.kaya.KayaApp;");
        w(b, "import dev.kaya.KayaSums;");
        w(b, "import java.util.function.BiConsumer;");
        w(b, "");
        w(b, "/** %s's generated sum surface: the collection factory and the", simple);
        w(b, " * staged eliminator — each stage offers exactly the next");
        w(b, " * constructor's arm, so totality is a compile error here, and");
        w(b, " * the scene checks it again. */");
        w(b, "final class %sKaya {", simple);

        // The factory: the one spelling of the constructor order.
        StringBuilder classes = new StringBuilder();
        for (TypeMirror sub : permitted) {
            classes.append(", ").append(refOf(sub, pkg)).append(".class");
        }
        w(b, "    static KayaSums.SumCollection<%s, %s> collection(KayaApp.Tx tx) {", key, sumRef);
        w(b, "        return KayaSums.sumOf(tx, %s.class%s);", sumRef, classes);
        w(b, "    }");
        w(b, "");

        // The staged eliminator.
        w(b, "    static Stage%d eachSum(KayaApp.Tx tx, KayaSums.SumCollection<%s, %s> c) {",
                permitted.size(), key, sumRef);
        w(b, "        return new Stage%d(tx, c, new KayaSums.Arm[%d]);",
                permitted.size(), permitted.size());
        w(b, "    }");
        for (int i = permitted.size(); i >= 1; i--) {
            TypeMirror sub = permitted.get(permitted.size() - i);
            String subRef = refOf(sub, pkg);
            String arm = lowerFirst(simpleOf(sub));
            int at = permitted.size() - i;
            w(b, "");
            w(b, "    static final class Stage%d {", i);
            w(b, "        private final KayaApp.Tx tx;");
            w(b, "        private final KayaSums.SumCollection<%s, %s> c;", key, sumRef);
            w(b, "        private final KayaSums.Arm<%s, %s>[] arms;", key, sumRef);
            w(b, "");
            w(b, "        Stage%d(KayaApp.Tx tx, KayaSums.SumCollection<%s, %s> c,", i, key, sumRef);
            w(b, "                KayaSums.Arm<%s, %s>[] arms) {", key, sumRef);
            w(b, "            this.tx = tx;");
            w(b, "            this.c = c;");
            w(b, "            this.arms = arms;");
            w(b, "        }");
            w(b, "");
            String next = i == 1 ? "KayaApp.Widget"
                    : "Stage" + (i - 1);
            w(b, "        %s %s(BiConsumer<KayaApp.Tpl, KayaSums.Case<%s, %s>> arm) {",
                    next, arm, key, subRef);
            w(b, "            arms[%d] = c.arm(%s.class, arm);", at, subRef);
            if (i == 1) {
                w(b, "            return KayaSums.eachSum(tx, c, arms);");
            } else {
                w(b, "            return new Stage%d(tx, c, arms);", i - 1);
            }
            w(b, "        }");
            w(b, "    }");
        }
        // The refined per-constructor patches: re-eliminate at call
        // time, then write through the witnessed update.
        for (TypeMirror sub : permitted) {
            TypeElement subElement =
                    (TypeElement) processingEnv.getTypeUtils().asElement(sub);
            String subRef = refOf(sub, pkg);
            String subName = simpleOf(sub);
            List<String[]> fields = new java.util.ArrayList<>();
            for (var component : subElement.getRecordComponents()) {
                String token = wireTokenType(component.asType().toString());
                if (token != null) {
                    fields.add(new String[] {
                            component.getSimpleName().toString(),
                            component.asType().toString().startsWith("java.lang.")
                                    ? token
                                    : component.asType().toString()});
                }
            }
            if (fields.isEmpty()) {
                continue;
            }
            w(b, "");
            w(b, "    /** as%s re-eliminates at call time: the Optional is the", subName);
            w(b, "     * refinement, fresh at write time — a stale occurrence folds");
            w(b, "     * into the empty — and each setter's update carries %s as", subName);
            w(b, "     * its witness, asserted again by the scene. */");
            w(b, "    static java.util.Optional<%sPatch> as%s(KayaApp.Tx tx,", subName, subName);
            w(b, "            KayaSums.SumCollection<%s, %s> c, %s key) {", key, sumRef, key);
            w(b, "        return c.get(tx, key) instanceof %s", subRef);
            w(b, "                ? java.util.Optional.of(new %sPatch(tx, c, key))", subName);
            w(b, "                : java.util.Optional.empty();");
            w(b, "    }");
            w(b, "");
            w(b, "    /** %s's refined patch: named setters over the witnessed", subName);
            w(b, "     * update. */");
            w(b, "    static final class %sPatch {", subName);
            w(b, "        private final KayaApp.Tx tx;");
            w(b, "        private final KayaSums.SumCollection<%s, %s> c;", key, sumRef);
            w(b, "        private final %s key;", key);
            w(b, "");
            w(b, "        %sPatch(KayaApp.Tx tx, KayaSums.SumCollection<%s, %s> c, %s key) {",
                    subName, key, sumRef, key);
            w(b, "            this.tx = tx;");
            w(b, "            this.c = c;");
            w(b, "            this.key = key;");
            w(b, "        }");
            for (String[] f : fields) {
                w(b, "");
                w(b, "        %sPatch %s(%s v) {", subName, f[0], f[1]);
                w(b, "            c.updateField(tx, key, %s.class, %s::%s, v);",
                        subRef, subRef, f[0]);
                w(b, "            return this;");
                w(b, "        }");
            }
            w(b, "    }");
        }
        w(b, "");
        w(b, "    private %sKaya() {}", simple);
        w(b, "}");

        Path path = Path.of(out, pkg.replace('.', '/'), simple + "Kaya.java");
        Files.createDirectories(path.getParent());
        // Write only on change: regeneration is idempotent AND
        // mtime-stable, so a gen run never invalidates a concurrent
        // build reading the checked-in file.
        if (!Files.exists(path) || !Files.readString(path).equals(b.toString())) {
            try (Writer writer = Files.newBufferedWriter(path)) {
                writer.write(b.toString());
            }
        }
        processingEnv.getMessager().printMessage(Diagnostic.Kind.NOTE,
                "kaya: wrote " + path + " (" + permitted.size() + " constructors of "
                        + simple + ")");
    }

    /** The wire vocabulary a component type maps into: boxed token
     * type. Any other component is guest-only and gets no token or
     * setter. */
    private static String wireTokenType(String type) {
        return switch (type) {
            case "java.lang.String" -> "String";
            case "boolean", "java.lang.Boolean" -> "Boolean";
            case "long", "java.lang.Long" -> "Long";
            case "double", "java.lang.Double" -> "Double";
            // The blob value type: KayaRecords.Info maps byte[]
            // components into the schema, so the generator must too —
            // a skipped component here would shift every later
            // exact-index token off the runtime schema.
            case "byte[]" -> "byte[]";
            default -> null;
        };
    }

    private void generateRecord(TypeElement rec, String out) throws IOException {
        String key = rec.getAnnotation(KayaGen.class).key();
        String pkg = processingEnv.getElementUtils().getPackageOf(rec)
                .getQualifiedName().toString();
        String recRef = qualifiedInPackage(rec, pkg);
        String simple = rec.getSimpleName().toString();

        // The wire fields: components of wire type, in declaration
        // order — the same indices every other tier stamps.
        record WireField(String name, String token, String param) {}
        List<WireField> fields = new java.util.ArrayList<>();
        for (var component : rec.getRecordComponents()) {
            String token = wireTokenType(component.asType().toString());
            if (token != null) {
                fields.add(new WireField(component.getSimpleName().toString(), token,
                        component.asType().toString().startsWith("java.lang.")
                                ? token
                                : component.asType().toString()));
            }
        }
        if (fields.isEmpty()) {
            processingEnv.getMessager().printMessage(Diagnostic.Kind.ERROR,
                    "kaya: " + simple + " has no wire-typed components", rec);
            return;
        }

        StringBuilder b = new StringBuilder();
        w(b, "package %s;", pkg);
        w(b, "");
        w(b, "// Code generated by the kaya annotation processor; DO NOT EDIT.");
        w(b, "// Regenerate with tools/gen-guests.sh (which also checks freshness).");
        w(b, "");
        w(b, "import dev.kaya.KayaApp;");
        w(b, "import dev.kaya.KayaRecords;");
        w(b, "");
        w(b, "/** %s's generated record surface: the collection factory,", simple);
        w(b, " * exact-index field tokens, and a named-setter patch (each set");
        w(b, " * records one update_field — a patch is recorded writes, never a");
        w(b, " * diff). */");
        w(b, "final class %sKaya {", simple);
        w(b, "    static KayaRecords.Collection<%s, %s> collection(KayaApp.Tx tx) {", key, recRef);
        w(b, "        return KayaRecords.collectionOf(tx, %s.class);", recRef);
        w(b, "    }");
        w(b, "");
        for (int i = 0; i < fields.size(); i++) {
            WireField f = fields.get(i);
            w(b, "    static final KayaRecords.Field<%s> %s = KayaRecords.fieldAt(%d);",
                    f.token(), upperSnake(f.name()), i);
        }
        w(b, "");
        w(b, "    static Patch patch(KayaApp.Tx tx, KayaRecords.Collection<%s, %s> c, %s key) {",
                key, recRef, key);
        w(b, "        return new Patch(c.patch(tx, key));");
        w(b, "    }");
        w(b, "");
        w(b, "    static final class Patch {");
        w(b, "        private final KayaRecords.Patch<%s, %s> p;", key, recRef);
        w(b, "");
        w(b, "        Patch(KayaRecords.Patch<%s, %s> p) {", key, recRef);
        w(b, "            this.p = p;");
        w(b, "        }");
        for (WireField f : fields) {
            w(b, "");
            w(b, "        Patch %s(%s v) {", f.name(), f.param());
            w(b, "            p.set(%s, v);", upperSnake(f.name()));
            w(b, "            return this;");
            w(b, "        }");
        }
        w(b, "    }");
        w(b, "");
        w(b, "    /** The record template, expression form: the body runs once,");
        w(b, "     * authoring the blueprint with the typed row surface");
        w(b, "     * (exact-index tokens, no probes); stamping is the core's");
        w(b, "     * replay. */");
        w(b, "    static KayaApp.Widget each(KayaApp.Tx tx, KayaRecords.Collection<%s, %s> c,",
                key, recRef);
        w(b, "            java.util.function.Consumer<Row> body) {");
        w(b, "        // A block body: an expression lambda is ambiguous between");
        w(b, "        // the Consumer and Function forEach overloads.");
        w(b, "        return tx.forEach(c.handle, t -> {");
        w(b, "            body.accept(new Row(t, c));");
        w(b, "        });");
        w(b, "    }");
        w(b, "");
        w(b, "    /** The for-each form: `for (var row : %sKaya.rows(c))` traces", simple);
        w(b, "     * the record template — the body runs once, and a break is");
        w(b, "     * caught at submit. */");
        w(b, "    static Iterable<Row> rows(KayaRecords.Collection<%s, %s> c) {", key, recRef);
        w(b, "        return KayaRecords.rowTrace(c, t -> new Row(t, c));");
        w(b, "    }");
        w(b, "");
        w(b, "    /** The row surface: the template handle plus one token per");
        w(b, "     * wire field, and the constructors that consume them. */");
        w(b, "    static final class Row {");
        w(b, "        private final KayaApp.Tpl t;");
        w(b, "        private final KayaRecords.Collection<%s, %s> c;", key, recRef);
        for (WireField f : fields) {
            w(b, "        final KayaRecords.Field<%s> %s = %s;",
                    f.token(), f.name(), upperSnake(f.name()));
        }
        w(b, "");
        w(b, "        Row(KayaApp.Tpl t, KayaRecords.Collection<%s, %s> c) {", key, recRef);
        w(b, "            this.t = t;");
        w(b, "            this.c = c;");
        w(b, "        }");
        w(b, "");
        w(b, "        KayaApp.Node label(KayaRecords.Field<String> f) {");
        w(b, "            return c.label(t, f);");
        w(b, "        }");
        w(b, "");
        w(b, "        KayaApp.Node image(KayaRecords.Field<byte[]> f) {");
        w(b, "            return c.image(t, f);");
        w(b, "        }");
        w(b, "");
        w(b, "        KayaApp.Node checkbox(KayaRecords.Field<Boolean> f,");
        w(b, "                KayaRecords.Collection.ToggleHandler<%s> onToggle) {", key);
        w(b, "            return c.checkbox(t, f, onToggle);");
        w(b, "        }");
        w(b, "");
        w(b, "        KayaApp.Node row(Runnable body) {");
        w(b, "            return t.row(body);");
        w(b, "        }");
        w(b, "");
        w(b, "        KayaApp.Node column(Runnable body) {");
        w(b, "            return t.column(body);");
        w(b, "        }");
        w(b, "    }");
        w(b, "");
        w(b, "    private %sKaya() {}", simple);
        w(b, "}");

        Path path = Path.of(out, pkg.replace('.', '/'), simple + "Kaya.java");
        Files.createDirectories(path.getParent());
        // Write only on change: regeneration is idempotent AND
        // mtime-stable, so a gen run never invalidates a concurrent
        // build reading the checked-in file.
        if (!Files.exists(path) || !Files.readString(path).equals(b.toString())) {
            try (Writer writer = Files.newBufferedWriter(path)) {
                writer.write(b.toString());
            }
        }
        processingEnv.getMessager().printMessage(Diagnostic.Kind.NOTE,
                "kaya: wrote " + path + " (" + fields.size() + " fields of " + simple + ")");
    }

    private static String upperSnake(String s) {
        StringBuilder out = new StringBuilder();
        for (char c : s.toCharArray()) {
            if (Character.isUpperCase(c) && out.length() > 0) {
                out.append('_');
            }
            out.append(Character.toUpperCase(c));
        }
        return out.toString();
    }

    /** A permitted type's reference from the generated file's package:
     * nested guest types keep their outer-class qualifier. */
    private String refOf(TypeMirror mirror, String pkg) {
        TypeElement element = (TypeElement) processingEnv.getTypeUtils().asElement(mirror);
        return qualifiedInPackage(element, pkg);
    }

    private String qualifiedInPackage(TypeElement element, String pkg) {
        String qualified = element.getQualifiedName().toString();
        return qualified.startsWith(pkg + ".") ? qualified.substring(pkg.length() + 1)
                : qualified;
    }

    private static String simpleOf(TypeMirror mirror) {
        String s = mirror.toString();
        return s.substring(s.lastIndexOf('.') + 1);
    }

    private static String lowerFirst(String s) {
        return Character.toLowerCase(s.charAt(0)) + s.substring(1);
    }

    private static void w(StringBuilder b, String f, Object... args) {
        b.append(String.format(f, args)).append('\n');
    }
}
