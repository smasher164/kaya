(* Reads assembled from a byte accessor (absolute offset -> byte);
   kaya v1 targets are all little-endian. *)
let u16_at byte i = byte i lor (byte (i + 1) lsl 8)
let u32_at byte i = u16_at byte i lor (u16_at byte (i + 2) lsl 16)

(* Decode one value at offset [at]; returns (value, next offset). *)
let parse_value byte at =
  let vtype = u32_at byte at in
  let vlen = u32_at byte (at + 4) in
  let next = at + 8 + ((vlen + 7) land lnot 7) in
  let v =
    if vtype = value_bool then Bool (byte (at + 8) <> 0)
    else if vtype = value_i64 || vtype = value_f64 || vtype = value_blob
    then begin
      let n = ref 0L in
      for i = 7 downto 0 do
        n := Int64.logor (Int64.shift_left !n 8)
               (Int64.of_int (byte (at + 8 + i)))
      done;
      if vtype = value_i64 then I64 !n
      else if vtype = value_blob then Blob !n
      else F64 (Int64.float_of_bits !n)
    end
    else Str (String.init vlen (fun i -> Char.chr (byte (at + 8 + i))))
  in
  (v, next)

(* Decode one occurrence record (header included) through the byte
   accessor. Some (kind, id, keys, payload) — keys is [] when id is
   a widget id, else id is a template node id and keys is the copy's
   key path; payload is Some for text_changed/toggled/value_changed
   (a Str, Bool, or F64
   value), None for clicks. None for pad/unknown kinds. *)
let parse_occurrence byte =
  let kind = u16_at byte 4 in
  if kind <> occ_kind_button_clicked && kind <> occ_kind_text_changed && kind <> occ_kind_toggled && kind <> occ_kind_value_changed && kind <> occ_kind_close_requested && kind <> occ_kind_window_closed && kind <> occ_kind_alert_result && kind <> occ_kind_entry_popped && kind <> occ_kind_back_requested then None
  else begin
    (* ids are guest-allocated and small; the low u32 is the story. *)
    let id = u32_at byte 8 in
    if kind = occ_kind_alert_result
    then
      (* The alert's one answer: id + u32 choice (the alert_choice values). *)
      Some (kind, Int64.of_int id, [], Some (I64 (Int64.of_int (u32_at byte 16))))
    (* Surface lifecycle records carry the surface id alone
       ( derived from the record shapes ). *)
    else if kind = occ_kind_close_requested || kind = occ_kind_window_closed || kind = occ_kind_entry_popped || kind = occ_kind_back_requested
    then Some (kind, Int64.of_int id, [], None)
    else begin
    let path_len = u32_at byte 16 in
    let keys = ref [] in
    let at = ref 24 in
    for _ = 1 to path_len do
      let v, next = parse_value byte !at in
      keys := v :: !keys;
      at := next
    done;
    let payload =
      if kind = occ_kind_text_changed || kind = occ_kind_toggled || kind = occ_kind_value_changed then
        Some (fst (parse_value byte !at))
      else None
    in
    Some (kind, Int64.of_int id, List.rev !keys, payload)
    end
  end
