let pad8 b =
  while Buffer.length b mod 8 <> 0 do
    Buffer.add_char b '\000'
  done

(* Values self-pad to 8: they concatenate inside record bodies. *)
let encode_value b v =
  (match v with
  | Bool x ->
      Buffer.add_int32_le b (Int32.of_int value_bool);
      Buffer.add_int32_le b 1l;
      Buffer.add_char b (if x then '\001' else '\000')
  | I64 x ->
      Buffer.add_int32_le b (Int32.of_int value_i64);
      Buffer.add_int32_le b 8l;
      Buffer.add_int64_le b x
  | F64 x ->
      Buffer.add_int32_le b (Int32.of_int value_f64);
      Buffer.add_int32_le b 8l;
      Buffer.add_int64_le b (Int64.bits_of_float x)
  | Str x ->
      Buffer.add_int32_le b (Int32.of_int value_str);
      Buffer.add_int32_le b (Int32.of_int (String.length x));
      Buffer.add_string b x
  | Blob x ->
      Buffer.add_int32_le b (Int32.of_int value_blob);
      Buffer.add_int32_le b 8l;
      Buffer.add_int64_le b x);
  pad8 b

(* A key path: {u32 count, u32 reserved, count values}. *)
(* A counted value sequence: a key path or a record. *)
let encode_values b vals =
  Buffer.add_int32_le b (Int32.of_int (List.length vals));
  Buffer.add_int32_le b 0l;
  List.iter (encode_value b) vals

(* A collection's element sum: per variant, counted value-type tags,
   padded to 8. A record collection is the one-variant case. *)
let encode_variant_schemas b variants =
  Buffer.add_int32_le b (Int32.of_int (List.length variants));
  Buffer.add_int32_le b 0l;
  List.iter
    (fun schema ->
      Buffer.add_int32_le b (Int32.of_int (List.length schema));
      List.iter (fun t -> Buffer.add_int32_le b (Int32.of_int t)) schema)
    variants;
  pad8 b

let finish kind fill =
  let b = Buffer.create 64 in
  Buffer.add_int32_le b 0l;
  Buffer.add_int16_le b kind;
  Buffer.add_int16_le b 0;
  fill b;
  pad8 b;
  let s = Bytes.of_string (Buffer.contents b) in
  Bytes.set_int32_le s 0 (Int32.of_int (Bytes.length s));
  Bytes.to_string s

